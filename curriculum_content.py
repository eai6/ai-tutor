CURRICULUM_CONTENT = {
    "FOOD_PRODUCTION": FOOD_PRODUCTION_MODULES,
    "CARPENTRY": CARPENTRY_MODULES,
    "ELECTRICAL": ELECTRICAL_MODULES,
    "ICT": ICT_MODULES
}

def get_module_content(module_code):
    """Get detailed content for a specific module"""
    for program, modules in CURRICULUM_CONTENT.items():
        for mod_id, mod_data in modules.items():
            if mod_id == module_code or mod_data.get("code") == module_code:
                return mod_data
    return None

def get_learning_outcomes(module_code):
    """Get learning outcomes for a module"""
    content = get_module_content(module_code)
    if content:
        return content.get("learning_outcomes", [])
    return []

def get_units(module_code):
    """Get units and topics for a module"""
    content = get_module_content(module_code)
    if content:
        return content.get("units", {})
    return {}

def get_competencies(module_code):
    """Get competencies for a module"""
    content = get_module_content(module_code)
    if content:
        return content.get("competencies", [])
    return []
